// Get admin contact info
export const getAdminContactInfo = async () => {
  try {
    const { data, error } = await supabase
      .from('admin_settings')
      .select('*')
      .single();

    if (error) {
      console.error('Error fetching admin contact info:', error);
      return {
        whatsapp_number: '',
        is_whatsapp_share_enabled: false,
        email: '',
        phone: '',
        whatsapp: '',
        telegram: '',
        notification_preferences: {
          email: false,
          sms: false,
          whatsapp: false,
          telegram: false
        }
      };
    }

    return data || {
      whatsapp_number: '',
      is_whatsapp_share_enabled: false,
      email: '',
      phone: '',
      whatsapp: '',
      telegram: '',
      notification_preferences: {
        email: false,
        sms: false,
        whatsapp: false,
        telegram: false
      }
    };
  } catch (error) {
    console.error('Error in getAdminContactInfo:', error);
    return {
      whatsapp_number: '',
      is_whatsapp_share_enabled: false,
      email: '',
      phone: '',
      whatsapp: '',
      telegram: '',
      notification_preferences: {
        email: false,
        sms: false,
        whatsapp: false,
        telegram: false
      }
    };
  }
};

// Save admin contact info
export const saveAdminContactInfo = async (whatsappNumber: string, isEnabled: boolean, info: any) => {
  try {
    const { data: existingData, error: fetchError } = await supabase
      .from('admin_settings')
      .select('id')
      .single();

    let result;
    if (existingData?.id) {
      // Update existing record
      result = await supabase
        .from('admin_settings')
        .update({
          whatsapp_number: whatsappNumber,
          is_whatsapp_share_enabled: isEnabled,
          whatsapp: whatsappNumber,
          updated_at: new Date().toISOString()
        })
        .eq('id', existingData.id);
    } else {
      // Insert new record
      result = await supabase
        .from('admin_settings')
        .insert({
          whatsapp_number: whatsappNumber,
          is_whatsapp_share_enabled: isEnabled,
          whatsapp: whatsappNumber,
          created_at: new Date().toISOString(),
          updated_at: new Date().toISOString()
        });
    }

    if (result.error) {
      throw new Error('Failed to save admin contact information');
    }

    return { success: true };
  } catch (error) {
    console.error('Error saving admin contact info:', error);
    throw error;
  }
};

